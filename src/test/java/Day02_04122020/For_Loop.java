package Day02_04122020;

public class For_Loop {
    public static void main(String[] args) {
        String [] states = new String [5];
        states [0] = "New York";
        states[1] = "California";
        states [2] = "Ohio";
        states [3] = "Arizona";
        states [4] = "Nevada";
        // defining the loop
        //print all the states from 0 to 3
        // i++ each time is adding +1 to i
        //for loop is a fix loop.
        for(int i = 0; i< states.length; i++)
            System.out.println("my states is " + states[i]);

        }//end of for loop












    }//end of method
// end of java class
